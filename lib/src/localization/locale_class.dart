  static LocaleClass fromString(String locale) {
    final normalizedLocale = locale.toLowerCase();
    for (final localeClass in LocaleClass.values) {
      if (localeClass.languageCode == normalizedLocale || localeClass.name == normalizedLocale) {
        return localeClass;
      }
    }
    return LocaleClass.en;
  }

